def XORWithParam2(p1, p2):
    p2Len = len(p2)
    for i in range(len(p1)):
        p1[i] = p1[i] ^ p2[i % p2Len]


def IncWithParam2(p1, p2):
    for i in range(len(p1)):
        p1[i] = (p1[i] + p2) & 0xff


def DecWithParam2(p1, p2):
    for i in range(len(p1)):
        p1[i] = (p1[i] - p2) & 0xff


